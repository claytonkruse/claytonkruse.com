<h1 class="text-2xl">Work History</h1>

<h2>Dimension Computer</h2>
<p>
	<a href="https://www.dimension-computer.com/">Dimension Computer</a> is a tech
	repair store that I worked at over the entirety of the summer after my freshman
	year of college. I had already set my sights on an internship for the summer, but
	that did not work out for me.
</p>
<p>
	I was overqualified for the position, and only required only minimal training.
	I recieved high praise for my performence in resolving both hardware and
	software related issues, and was commended for my communication skills.
</p>
<p>
	Towards the end of the summer, I was put in charge of running the store for
	two weeks while the owner went on vacation. Upon his return, I was
	congradulated on the exemplery job I had done in the position.
</p>

<h2>Dierbergs</h2>
<p>
	My second job was working full time at Dierberg's in the Deli department. This
	job was much the same as working at Dairy Queen. Both jobs had lots of hard
	work preparing food, cleaning, and working the closing shift. The difference
	with Dierberg's was that I was now working full time while still attending
	school, and in a position where I had to both provide customer service, and
	functionally work as a team with my co-workers, which was in stark contrast to
	the solitude of Dairy Queen. During my time at Dierberg's, I was also in
	charge of training new part-time hires.
</p>

<h2>Dairy Queen</h2>
<p>
	My first job was working part time at Dairy Queen in the back, A.K.A. the
	grill. I prepared all the hot food at that location. My responsibilities
	included managing a grill, multiple fryers, multiple microwaves, two ovens,
	and the freezer. I always worked the closing shift, so at the end of the
	night, I would do all the dishes, clean the grill and the fryers, take the
	grease out back behind the dumpster and pour it into the receptacal, clean all
	surfaces including the insides of the microwaves, take out the trash, and
	finally mop the floors.
</p>
